# Left shifts for key schedule
SHIFT = [1, 1, 2, 2, 2, 2, 2, 2, 1, 2, 2, 2, 2, 2, 2, 1]


# Function for initial permutation
def initial_permutation(block):
    return permute(block, IP)


# Function for final permutation
def final_permutation(block):
    return permute(block, FP)


# Permutation function
def permute(block, table):
    permuted = 0
    for i in table:
        permuted <<= 1
        permuted |= (block >> (64 - i)) & 1
    return permuted


# Splitting the block into two halves
def split_block(block):
    left = (block >> 32) & 0xFFFFFFFF
    right = block & 0xFFFFFFFF
    return left, right


# Key generation (applying PC-1, left shifts, and PC-2)
def generate_round_keys(key):
    key = permute(key, PC1)
    round_keys = []
    left, right = split_block(key)
    for shift in SHIFT:
        left = ((left << shift) & 0xFFFFFFF) | (left >> (28 - shift))
        right = ((right << shift) & 0xFFFFFFF) | (right >> (28 - shift))
        round_key = (left << 28) | right
        round_keys.append(permute(round_key, PC2))
    return round_keys


# Expansion function for the right half
def expand_right_half(right_half):
    return permute(right_half, E)


# XOR with round key
def xor_with_round_key(expanded_half, round_key):
    return expanded_half ^ round_key


# S-Box substitution (use all 8 S-boxes)
def sbox_substitution(xored_output):
    result = 0
    for i in range(8):
        # Get 6 bits for the current S-box
        block = (xored_output >> (42 - i * 6)) & 0x3F
        row = ((block >> 5) << 1) | (block & 1)
        col = (block >> 1) & 0xF
        result <<= 4
        result |= S_BOX[i][row][col]
    return result


# Permutation after S-box substitution
def permutation(block):
    return permute(block, P)


# One round of DES
def des_round(left_half, right_half, round_key):
    expanded_right = expand_right_half(right_half)
    xored = xor_with_round_key(expanded_right, round_key)
    substituted = sbox_substitution(xored)
    permuted = permutation(substituted)
    new_right = left_half ^ permuted
    return right_half, new_right


# DES encryption
def des_encrypt(plaintext, key):
    permuted = initial_permutation(plaintext)
    left, right = split_block(permuted)
    round_keys = generate_round_keys(key)
    for i in range(16):
        left, right = des_round(left, right, round_keys[i])
    combined = (right << 32) | left
    return final_permutation(combined)


